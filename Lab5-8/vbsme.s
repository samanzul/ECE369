        addi $t8, $v0, 0    # t8 = sum from SAD
        lw $a3, 0($sp)      # restore current index to a3
        addi $sp, $sp, 4    # restore stack
        jal SAD
        addi $t9, $v0, 0    # t9 = sum from SAD
        sgt $t8, $t8, $t9   # t8 = 1 if t8 > t9
